/**
 * Returns raw SVG icon wrapping it with target CSS classes and sizes
 * @param {string} name 
 * @param {string} className 
 * @param {number} size 
 */
export function getIcon(name, className = '', size = 24) {
  const iconSvg = ICONS[name] || ICONS.bolt;
  return iconSvg
    .replace('width="24"', `width="${size}"`)
    .replace('height="24"', `height="${size}"`)
    .replace('class="lucide ', `class="lucide ${className} `);
}

/**
 * Formats a Date object into HH:MM:SS PM format
 * @param {Date} date 
 */
export function formatTime(date) {
  return date.toLocaleTimeString([], { hour: '2-digit', minute: '2-digit', second: '2-digit' });
}

/**
 * Formats a Date object into standard readable string
 * @param {Date} date 
 */
export function formatDate(date) {
  return date.toLocaleDateString([], { weekday: 'short', month: 'short', day: 'numeric', year: 'numeric' });
}
